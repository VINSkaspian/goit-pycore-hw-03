import re

def normalize_phone(phone_number):
    """
    Корегує телефонний номер до міжнародного формату.
    - Видаляє всі символи, крім цифр та '+' на початку.
    - Додає код країни '+38' для українських номерів, якщо він відсутній.
    Параметри:
    phone_number (str): Повертає номер телефону в будь-якому форматі.
    Повертає:
    str: Нормалізований номер телефону в форматі '+380XXXXXXXXX'.
    """
    # Видаляємо всі символи, крім цифр
    digits = re.sub(r'\D', '', phone_number)
    # Перевіряємо, чи номер вже містить міжнародний код
    if digits.startswith('380'):
        return '+{}'.format(digits)
    elif digits.startswith('0'):
        return '+38{}'.format(digits)
    else:
        # Якщо формат зовсім інший, додаємо +38 просто перед усім
        return '+38{}'.format(digits)
raw_numbers = [
    "067\t123 4567",
    "(095) 234-5678\n",
    "+380 44 123 4567",
    "380501234567",
    "    +38(050)123-32-34",
    "     0503451234",
    "(050)8889900",
    "38050-111-22-22",
    "38050 111 22 11   ",
]
sanitized_numbers = [normalize_phone(num) for num in raw_numbers]
print("Номери телефонів для SMS-розсилки:", sanitized_numbers)
